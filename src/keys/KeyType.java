package keys;

/**
 * The different key types. (see each type for details).
 * @author CLOVIS
 */
public enum KeyType {
    /** A char. */
    CHAR,
    
    /** Either 'ENTER' or 'RETURN' key. */
    ENTER,
    
    /** The 'BACKSPACE' key. */
    BACKSPACE,
    
    /** The 'DELETE' key. */
    DELETE,
    
    /** The arrow keys. */
    ARROW,
    
    /** The 'ESCAPE' or 'ESC' key. */
    ESC,
    
    /** A 'TAB' or tabulation. */
    TAB,
    
    /** The 'ALT' key. */
    ALT,
    
    /** The 'CTRL' key. */
    CTRL,
    
    /** The 'SHIFT' key. */
    SHIFT,
    
    /** Any other key. */
    OTHER
}
